    private double getDouble(String text) {
        double valor;
        try {
            valor = Double.parseDouble(text);
        } catch (NumberFormatException e) {
            try {
                text = text.replace(",", ".");
                valor = Double.parseDouble(text);
            } catch (NumberFormatException f) {
                return 0.0; // retorna 0 se houver erro
            }
        }
        return valor;
    }
    
