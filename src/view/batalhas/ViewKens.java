    private int validarValor(String valor) {
        if (valor == null) {
            return 0;
        }
        try {
            return Integer.parseInt(valor);
        } catch (NumberFormatException e) {
            
            JOptionPane.showMessageDialog(null, "Entre com um valor inteiro v√°lido!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            
            return -1;
        }
    }
    
    private void aplicar() {
        int ryu;
        if ((ryu = validarValor(jTextField.getText())) == -1) {
            return;
        }
        
        DefaultTableModel dtm = (DefaultTableModel) jTable.getModel();
        Object[] valores = new Object[2];
        
        // limpando rows (linhas)
        // contando quandos mobs existem
        int qtdMobs = 0;
        for (String nome : AlgoritmoPrincipal.getFichas().keySet()) {
            if (AlgoritmoPrincipal.getPerson(nome).isCriaturaDoMestre()) {
                qtdMobs++;
            }
        }
        for (int i = 0; i < AlgoritmoPrincipal.getFichas().size() - qtdMobs; i++) {
            dtm.removeRow(i);
        }
        
        // setando valores
        for (String nome : AlgoritmoPrincipal.getFichas().keySet()) {
            if (AlgoritmoPrincipal.getPerson(nome).isCriaturaDoMestre()) {
                continue;
            }
            valores[0] = nome;
            valores[1] = Calculadora.getRen(AlgoritmoPrincipal.getPerson(nome), ryu);
            dtm.addRow(valores);
        }
    }
    
