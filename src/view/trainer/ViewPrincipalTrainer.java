    private void treinarPer() {
        String nomePer = jComboBoxPer.getSelectedItem().toString();
        if (nomePer.equals("NOVA PERICIA")) {
            // testando se o usuario entrou com algum nome
            nomePer = jTextFieldPer.getText().toUpperCase();
            if (nomePer.equals("")) {
                JOptionPane.showMessageDialog(null, "Insira um nome para a nova perícia",
                        "Invalid Name!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (nomePer.equals("NENHUMA") || nomePer.equals("NULL") || nomePer.equals("NOVA")) {
                JOptionPane.showMessageDialog(null, "Esse nome não é válido!",
                        "Error!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // testando se a pericia já existe
            boolean periciaExistente = false;
            for (String nome : AlgoritmoPrincipalTrainer.getFicha().getPer().keySet()) {
                if (nome.equals(nomePer)) {
                    periciaExistente = true;
                    break;
                }
            }
            
            if (periciaExistente == false) {
                // inserindo nova perícia
                LvlBonus lvlBonus = new LvlBonus();
                AlgoritmoPrincipalTrainer.getFicha().putPer(nomePer, lvlBonus);
            }
        }
        
        // preparando algoritmo principal para o treino
        AlgoritmoPrincipalTrainer.setNomeTreino(nomePer);
        AlgoritmoPrincipalTrainer.setTreinar(AlgoritmoPrincipalTrainer.getPER());
        
        // limpando nome
        jTextFieldPer.setText("");
        
        new View_treinoProEPer(null, true).setVisible(true);
    }
    
