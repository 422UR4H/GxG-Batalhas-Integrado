    private void attExpNen(int exp) {
        exp += Integer.parseInt(jTextFieldExpNenAtual.getText());
        jTextFieldExpNenAtual.setText(String.valueOf(exp));
        if (distribLivre) {
            int lvlNen = CalculadoraGerador.calculaLvlNen(exp);
            jTextFieldExpNenMax.setText(String.valueOf(lvlNen));
            jLabelLvlNen.setText(String.valueOf(lvlNen));
        } else {
            jProgressBarExpNen.setValue(exp);
        }
    }
    
