    // calcula novos valores de bonus
    private int calculaBonus(int valAtual, boolean valIncrementado) {
        int valAnterior;
        if (valIncrementado) {
            valAnterior = valAtual - 1;
        } else {
            valAnterior = valAtual + 1;
        }
        return ((valAtual - 10) / 10) - ((valAnterior - 10) / 10);
    }
    
