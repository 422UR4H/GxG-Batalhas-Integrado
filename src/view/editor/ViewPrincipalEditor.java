    private void jButtonDelPerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDelPerActionPerformed
        Ficha ficha = AlgoritmoPrincipalEditor.getFicha();
        
        if (jComboBoxPer.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Não há perícia selecionada!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        switch (JOptionPane.showConfirmDialog(null, "Deletar Perícia?", "Del per?", JOptionPane.YES_NO_OPTION)) {
            case 0:
                // deletando pericia
                ficha.getPer().remove(jComboBoxPer.getSelectedItem().toString());
                
                // atualizando comboBox
                jComboBoxPer.removeAllItems();
                for (String nome : ficha.getPer().keySet()) {
                    jComboBoxPer.addItem(nome);
                }
        }
    }//GEN-LAST:event_jButtonDelPerActionPerformed

    private void jButtonDelProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDelProActionPerformed
        Ficha ficha = AlgoritmoPrincipalEditor.getFicha();
        String pro = jComboBoxPro.getSelectedItem().toString();
        
        if (pro.equals("VEL") || pro.equals("ESQ") || pro.equals("INI") ||
            pro.equals("APR") || pro.equals("CRT") || pro.equals("HP")) {
            
            JOptionPane.showMessageDialog(null, "Não se pode deletar essas proficiências!",
                    "Error!", JOptionPane.ERROR_MESSAGE);
            
            return;
        }

        if (jComboBoxPro.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(null, "Não há proficiência selecionada!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        switch (JOptionPane.showConfirmDialog(null, "Deletar Proficiência?", "Del pro?", JOptionPane.YES_NO_OPTION)) {
            case 0:
                // deletando proficiencia
                ficha.getPro().remove(pro);
                
                // atualizando comboBox
                jComboBoxPro.removeAllItems();
                for (String nome : ficha.getPro().keySet()) {
                    jComboBoxPro.addItem(nome);
                }
        }
    }//GEN-LAST:event_jButtonDelProActionPerformed

