    private void comecarTreino() {
        // lendo o nome do personagem
        // retorna true se atacante for um nome valido
        Ficha ficha;
        String nomePer = jTextFieldNome.getText().toUpperCase();
        if (AlgoritmoPrincipalTrainer.containsFicha(nomePer)) {
            ficha = AlgoritmoPrincipalTrainer.getFicha(nomePer);
        } else {
            JOptionPane.showMessageDialog(null, "Não foi encontrado um personagem com esse nome",
                    "Error!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // salvando nome (chave) da ficha
        AlgoritmoPrincipalTrainer.setNome(nomePer);
        
        AlgoritmoPrincipalTrainer.setFicha(ficha);
        AlgoritmoPrincipalTrainer.initTreino();
        initDias();
        atualizarCampos();
        
        // setando categoria nen
        selecionarNen();
    }
    
