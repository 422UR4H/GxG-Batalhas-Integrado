    private void initVetor(Object[] vetor, String categoria, Ficha personagem) {
        switch(categoria) {
            case "Reforco":
                vetor[1] = personagem.getAtrRef();
                vetor[2] = personagem.getModRef();
                vetor[3] = personagem.getPerRef();
                break;
            case "Emissao":
                vetor[1] = personagem.getAtrEms();
                vetor[2] = personagem.getModEms();
                vetor[3] = personagem.getPerEms();
                break;
            case "Manipulacao":
                vetor[1] = personagem.getAtrMan();
                vetor[2] = personagem.getModMan();
                vetor[3] = personagem.getPerMan();
                break;
            case "Materializacao":
                vetor[1] = personagem.getAtrMat();
                vetor[2] = personagem.getModMat();
                vetor[3] = personagem.getPerMat();
                break;
            case "Especialista":
                vetor[1] = personagem.getAtrEsp();
                vetor[2] = personagem.getModEsp();
                vetor[3] = personagem.getPerEsp();
                break;
            case "Transformacao":
                vetor[1] = personagem.getAtrTrs();
                vetor[2] = personagem.getModTrs();
                vetor[3] = personagem.getPerTrs();
                break;
        }
    }
    
    private void jComboBoxNenItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBoxNenItemStateChanged
        String categoria = jComboBoxNen.getSelectedItem().toString();
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dtm.getRowCount();
        
        // limpando jTable
        for (int i = 0; i < rowCount; i++) {
            dtm.removeRow(0);
        }
        
        // reconstruindo jTable
        for (String nick: AlgoritmoPrincipal.getFichas().keySet()) {
            Ficha personagem = AlgoritmoPrincipal.getPerson(nick);
            
            // controlando para nao mostrar MOBs
            if (personagem.isCriaturaDoMestre()) {
                continue;
            }
            
            // setando linhas
            if (personagem.getNen().equals(categoria)) {
                Object[] vetor = new Object[4];
                vetor[0] = nick;
                // setando valor, modificador e pericia no vetor dependendo da categoria
                initVetor(vetor, categoria, personagem);
                
                // setando valores na tabela
                dtm.addRow(vetor);
            }
        }
    }//GEN-LAST:event_jComboBoxNenItemStateChanged

