    private void setValores() {
        DefaultTableModel dtm = (DefaultTableModel) jTable.getModel();
        Object[] titulo = new Object[3];
        Object[] valores = new Object[3];
        int rowCount = dtm.getRowCount();
        
        
        // limpando jTable
        for (int i = 0; i < rowCount; i++) {
            dtm.removeRow(0);
        }
            
        // se o usuario quiser ver os mops
//        switch ()
        if (info.equals(AlgoritmoPrincipal.getMOP())) {
            // nomeando os titulos das colunas
            titulo[0] = "Nick";
            titulo[1] = "Mop Maximo";
            titulo[2] = "Mop Atual";
            dtm.setColumnIdentifiers(titulo);
            
            // setando valores
            for (String nome : AlgoritmoPrincipal.getFichas().keySet()) {
                if (AlgoritmoPrincipal.getPerson(nome).isCriaturaDoMestre()) {
                    continue;
                }
                // controlando personagens
                if (!AlgoritmoPrincipal.getPerson(nome).isPersonagemDeJogador() && jCheckBox.isSelected()) {
                    continue;
                }
                
                valores[0] = AlgoritmoPrincipal.getPerson(nome).getNick();
                valores[1] = AlgoritmoPrincipal.getPerson(nome).getMopMax();
                valores[2] = AlgoritmoPrincipal.getPerson(nome).getMopAtual();
                dtm.addRow(valores);
            }
        } else if (info.equals(AlgoritmoPrincipal.getEXT())) {
            // nomeando os titulos das colunas
            titulo[0] = "Nick";
            titulo[1] = "Extensão";
            titulo[2] = "REN perdido";
            dtm.setColumnIdentifiers(titulo);
            
            // calculando e setando valores
            for (String nome : AlgoritmoPrincipal.getFichas().keySet()) {
                if (AlgoritmoPrincipal.getPerson(nome).isCriaturaDoMestre()) {
                    continue;
                }
                // controlando personagens
                if (!AlgoritmoPrincipal.getPerson(nome).isPersonagemDeJogador() && jCheckBox.isSelected()) {
                    continue;
                }
                
                // calculando ren perdido
                int renPerdido = Calculadora.getRenLivre(AlgoritmoPrincipal.getPerson(nome), 100);
                renPerdido -= Calculadora.getRen(AlgoritmoPrincipal.getPerson(nome), 100);
                
                // setando valores
                valores[0] = AlgoritmoPrincipal.getPerson(nome).getNick();
                valores[1] = AlgoritmoPrincipal.getPerson(nome).getCorpo().getValor();
                valores[2] = renPerdido;
                dtm.addRow(valores);
            }
        } else if (info.equals(AlgoritmoPrincipal.getHP())) { // hps
            // nomeando os titulos das colunas
            titulo[0] = "Nick";
            titulo[1] = "Hp Maximo";
            titulo[2] = "Hp Atual";
            dtm.setColumnIdentifiers(titulo);
            
            // setando valores
            for (String nome : AlgoritmoPrincipal.getFichas().keySet()) {
                if (AlgoritmoPrincipal.getPerson(nome).isCriaturaDoMestre()) {
                    continue;
                }
                // controlando personagens
                if (!AlgoritmoPrincipal.getPerson(nome).isPersonagemDeJogador() && jCheckBox.isSelected()) {
                    continue;
                }
                
                valores[0] = AlgoritmoPrincipal.getPerson(nome).getNick();
                valores[1] = AlgoritmoPrincipal.getPerson(nome).getHpMax();
                valores[2] = AlgoritmoPrincipal.getPerson(nome).getHpAtual();
                dtm.addRow(valores);
            }
        } else if (info.equals(AlgoritmoPrincipal.getLVL())) {
            // nomeando os titulos das colunas
            titulo[0] = "Nick";
            titulo[1] = "Lvl";
            titulo[2] = "Lvl Nen";
            dtm.setColumnIdentifiers(titulo);
            
            // setando valores
            for (String nome : AlgoritmoPrincipal.getFichas().keySet()) {
                if (AlgoritmoPrincipal.getPerson(nome).isCriaturaDoMestre()) {
                    continue;
                }
                // controlando personagens
                if (!AlgoritmoPrincipal.getPerson(nome).isPersonagemDeJogador() && jCheckBox.isSelected()) {
                    continue;
                }
                
                valores[0] = AlgoritmoPrincipal.getPerson(nome).getNick();
                valores[1] = AlgoritmoPrincipal.getPerson(nome).getLvl();
                valores[2] = AlgoritmoPrincipal.getPerson(nome).getLvlNen();
                dtm.addRow(valores);
            }
        }
    }
    
