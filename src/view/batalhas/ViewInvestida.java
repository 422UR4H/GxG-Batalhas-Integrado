    private int validarValor(String valor) {
        try {
            if (valor.equals("")) {
                return 0;
            }
            return Integer.parseInt(valor);
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Entre com um valor inteiro v√°lido!",
                    "Error!", JOptionPane.ERROR_MESSAGE);
            
            return -1;
        }
    }
    
    private void jButtonAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAplicarActionPerformed
        Calculadora.setInvestidaAtacante(Integer.valueOf(jLabelMsCalculadaAtacante.getText()));
        Calculadora.setInvestidaDefensor(Integer.valueOf(jLabelMsCalculadaDefensor.getText()));
        Calculadora.setRyuInvestidaAtacante(Integer.valueOf(jLabelMsNenAtacante.getText()));
        Calculadora.setRyuInvestidaDefensor(Integer.valueOf(jLabelMsNenDefensor.getText()));
        
        Calculadora.calculaInvestida();
        
        JOptionPane.showMessageDialog(null, "Investida Total: " + Calculadora.getInvestida());
        this.dispose();
    }//GEN-LAST:event_jButtonAplicarActionPerformed

    private void setVelAtacante() {
        int valor;
        if ((valor = validarValor(jTextFieldValorVelAtacante.getText())) == -1) {
            jTextFieldValorVelAtacante.setText("0");
            return;
        }
        if (valor == 0) {
            jTextFieldValorVelAtacante.setText("0");
        }

        jLabelKmCalculadaAtacante.setText(String.valueOf(valor));
        jLabelMsCalculadaAtacante.setText(String.valueOf((int) Math.floor((double) valor / 3.6)));
    }
    
