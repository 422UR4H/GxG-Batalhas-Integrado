    private void jButtonAplicarExpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAplicarExpActionPerformed
        try {
            AlgoritmoPrincipalTrainer.getFicha().setExpBonus(Integer.parseInt(jTextFieldExpBonus.getText()));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Valor inválido de exp bonus", "Error!", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        // inserindo exp na ficha
        try {
            // testando se o personagem upou (o metodo retorna true, caso positivo)
            if (CalculadoraTrainer.putExpFicha(Integer.parseInt(jTextFieldSomarExp.getText()), AlgoritmoPrincipalTrainer.getFicha())) {
                // se o personagem tiver upado...
                new View_distribuir(null, true).setVisible(true);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Valor inválido de exp adicional", "Error!", JOptionPane.WARNING_MESSAGE);
        }
        
        // atualizando formulário
        jLabelLvl.setText(String.valueOf(AlgoritmoPrincipalTrainer.getFicha().getLvl()));
        jLabelLvlNen.setText(String.valueOf(AlgoritmoPrincipalTrainer.getFicha().getLvlNen()));
        jProgressBarLvl.setMaximum(AlgoritmoPrincipalTrainer.getEXP_LVL(AlgoritmoPrincipalTrainer.getFicha().getLvl() + 1));
        jProgressBarLvl.setValue(AlgoritmoPrincipalTrainer.getFicha().getExp());
        
        // limpando jTextField
        jTextFieldSomarExp.setText("0");
    }//GEN-LAST:event_jButtonAplicarExpActionPerformed

