    private void comecar() {
        // lendo o nome do personagem
        // retorna true se atacante for um nome valido
        Ficha ficha;
        String nomePer = jTextFieldNick.getText().toUpperCase();
        if (AlgoritmoPrincipalEditor.containsPersonagem(nomePer)) {
            ficha = AlgoritmoPrincipalEditor.getFicha(nomePer);
        } else {
            JOptionPane.showMessageDialog(null, "Não foi encontrado um personagem com esse nome",
                    "Error!", JOptionPane.WARNING_MESSAGE);
            
            jTextFieldNick.selectAll();
            return;
        }
        
        // carregando algoritmo principal
        // setando ficha
        AlgoritmoPrincipalEditor.setFicha(ficha);
        // setando chave
        AlgoritmoPrincipalEditor.setNome(ficha.getNick());
        
        // setando informações nas jTexts
        jTextFieldNovoNick.setText(ficha.getNick());
        jTextFieldDias.setText(String.valueOf(ficha.getDias()));
        jTextFieldPeso.setText(String.valueOf(ficha.getPeso()));
        jTextFieldIdade.setText(String.valueOf(ficha.getIdade()));
        jTextFieldAltura.setText(String.valueOf(ficha.getAltura()));
        
        jCheckBoxPersonagemDeJogador.setSelected(ficha.isPersonagemDeJogador());
        jCheckBoxCriaturaDoMestre.setSelected(ficha.isCriaturaDoMestre());
        
        jComboBoxHunter.setSelectedItem(ficha.getHunter());
        jComboBoxTendencia.setSelectedItem(ficha.getTendencia());
        jComboBoxNen.setSelectedItem(ficha.getNen());
        
        // setando breve descrição do personagem
        jTextPaneBreveDescPer.setText(ficha.getDescricaoBreve());
        
        // carregando jComboBoxes
        jComboBoxPer.removeAllItems();
        for (String nome : ficha.getPer().keySet()) {
            jComboBoxPer.addItem(nome);
        }
        
        jComboBoxPro.removeAllItems();
        for (String nome : ficha.getPro().keySet()) {
            jComboBoxPro.addItem(nome);
        }
        
        jComboBoxHab.removeAllItems();
        for (String nome : ficha.getHabilidades().keySet()) {
            jComboBoxHab.addItem(nome);
        }
    }
    
    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        String nick = jTextFieldNovoNick.getText().toUpperCase();
        // testando se o possível novo nome está disponível
        if (!jTextFieldNovoNick.getText().toUpperCase().equals(jTextFieldNick.getText().toUpperCase())) {
            if (AlgoritmoPrincipalEditor.containsPersonagem(nick) || nick.equals("NULL") || nick.equals("NOVO")) {

                JOptionPane.showMessageDialog(null, "Nome indisponível!", "Error!", JOptionPane.WARNING_MESSAGE);
                jTextFieldNovoNick.selectAll();
                return;
            }
        }
        
        Ficha ficha = AlgoritmoPrincipalEditor.getFicha();
//        boolean mudouNick = !ficha.getNick().equals(nick);
        
        // setando informações na ficha
        ficha.setNick(nick);
        ficha.setDias(Double.parseDouble(jTextFieldDias.getText()));
        ficha.setPeso(Double.parseDouble(jTextFieldPeso.getText().replace(',', '.')));
        ficha.setIdade(Integer.parseInt(jTextFieldIdade.getText()));
        ficha.setAltura(Double.parseDouble(jTextFieldAltura.getText().replace(',', '.')));
        
        ficha.setPersonagemDeJogador(jCheckBoxPersonagemDeJogador.isSelected());
        ficha.setCriaturaDoMestre(jCheckBoxCriaturaDoMestre.isSelected());
        
        ficha.setHunter(jComboBoxHunter.getSelectedItem().toString());
        ficha.setTendencia(jComboBoxTendencia.getSelectedItem().toString());
        ficha.setNen(jComboBoxNen.getSelectedItem().toString());
        
        // setando breve descrição
        ficha.setDescricaoBreve(jTextPaneBreveDescPer.getText());
        
        // atualizando atributos dependentes
        AlgoritmoPrincipalEditor.atualizaFicha(ficha);
        
        // substituindo ficha antiga pela editada
//        AlgoritmoPrincipalEditor.replaceFicha(AlgoritmoPrincipalEditor.getNome(), ficha);
        // isso não é mais feito automaticamente. agora a ficha é inserida
        AlgoritmoPrincipalEditor.putFicha(ficha);
        
        
            // salvando ficha
//        if (mudouNick) {
//            AlgoritmoPrincipalEditor.saveFichas();
//        } else {
//            AlgoritmoPrincipalEditor.saveFicha(ficha);
//        }
        try {
            AlgoritmoPrincipalEditor.saveFicha(ficha);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ViewPrincipalEditor.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        // mensagem ao usuario
        JOptionPane.showMessageDialog(null, "Ficha Salva!", "Success!", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButtonSalvarActionPerformed

