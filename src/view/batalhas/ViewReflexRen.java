    private double validarValor(String valor) {
        if (valor == null) {
            return 0.0;
        }
        try {
            valor = valor.replace(",", ".");
            return Double.parseDouble(valor);
        } catch (NumberFormatException e) {
            
            JOptionPane.showMessageDialog(null, "Entre com um valor real (IR) válido!",
                    "Error", JOptionPane.ERROR_MESSAGE);
            
            return -1.0;
        }
    }
    
    private void calcularRen() {
        double coef;
        if ((coef = validarValor(jTextFieldRen.getText())) == -1.0) {
            return;
        }
        coef /= 100.0;
        
        Ficha ficha = AlgoritmoPrincipal.getPerson(jLabelNome.getText());
        String nen = jComboBoxNen.getSelectedItem().toString();
        
        int renPen = (int) ((1 - coef) * Calculadora.getRen(ficha, 100, nen));
        
        // setando calculos realizados nas label
        jLabelPen.setText(String.valueOf(renPen));
        
        // habilitando jButton
        jButtonAbrirNovoRyu.setEnabled(true);
    }
    
    private void jTextFieldRenKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldRenKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            calcularRen();
        }
    }//GEN-LAST:event_jTextFieldRenKeyPressed

