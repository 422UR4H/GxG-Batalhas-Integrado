    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        Ficha ficha = AlgoritmoPrincipalGerador.getFicha();
        
        if (!distribLivre) {
            int expMax = Integer.parseInt(jTextFieldExpNenMax.getText());
            int expAtual = Integer.parseInt(jTextFieldExpNenAtual.getText());

            // conferindo exp distribuída
            if (expAtual > expMax) {
                JOptionPane.showMessageDialog(null, "Exp máxima atual ultrapassada!\n"
                        + "Subtraia pontos!", "Error!", JOptionPane.ERROR_MESSAGE);

                return;
            }
            if (expAtual < expMax) {
                int resposta = JOptionPane.showConfirmDialog(null, "Ainda há exp a ser distribuída!\n"
                        + "Deseja salvar mesmo assim?", "Warning!", JOptionPane.YES_NO_CANCEL_OPTION);

                switch (resposta) {
                    case JOptionPane.NO_OPTION:
                        return;
                    case JOptionPane.CANCEL_OPTION:
                        return;
                }
            }
        } else {
            // calculando exp
            int exp = 0;

            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValRef.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValEms.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValMat.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValMan.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValTrs.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValEsp.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValCdn.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValRyu.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValTen.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValAop.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValZts.getText()));
            exp += CalculadoraGerador.calculaExp(Integer.parseInt(jLabelValEn.getText()));
            exp += CalculadoraGerador.calculaExpMop(Integer.parseInt(jLabelValMop.getText()));

            // calculando lvlNen
            int i;
            for (i = 0; exp >= 0; i++) {
                exp -= AlgoritmoPrincipalGerador.getLVL_NEN(i + 1);
            }
            i--;
            exp += AlgoritmoPrincipalGerador.getLVL_NEN(i + 1);
            // setando lvlNen e expNen em ficha
            ficha.setLvlNen(i);
            ficha.setExpNen(exp);
            
            // setando em Dados Calculados
            jLabelLvlNen.setText(String.valueOf(i));
            jLabelExpNen.setText(String.valueOf(exp));
        }
        
        // SETANDO NOVOS DADOS
        // atributos de nen
        ficha.setAtrRef(Integer.parseInt(jLabelValRef.getText()));
        ficha.setAtrEms(Integer.parseInt(jLabelValEms.getText()));
        ficha.setAtrMat(Integer.parseInt(jLabelValMat.getText()));
        ficha.setAtrMan(Integer.parseInt(jLabelValMan.getText()));
        ficha.setAtrTrs(Integer.parseInt(jLabelValTrs.getText()));
        ficha.setAtrEsp(Integer.parseInt(jLabelValEsp.getText()));
        ficha.setAtrCdn(Integer.parseInt(jLabelValCdn.getText()));
        ficha.setTen(Integer.parseInt(jLabelValTen.getText()));
        ficha.setRyu(Integer.parseInt(jLabelValRyu.getText()));
        ficha.setAop(Integer.parseInt(jLabelValAop.getText()));
        ficha.setMop(Integer.parseInt(jLabelValMop.getText()));
        ficha.setZts(Integer.parseInt(jLabelValZts.getText()));
        ficha.setEn(Integer.parseInt(jLabelValEn.getText()));
        
        ficha.setModCdn();
        
        // atualizando dados
//        File folder = new File(algoritmos.batalhas.AlgoritmoPrincipal.getDIR_FICHAS());
//        try {
//            AlgoritmoPrincipalGerador.loadFichas(folder);
//        } catch (ClassNotFoundException ex) {
//            Logger.getLogger(ViewFicha.class.getName()).log(Level.SEVERE, null, ex);
//        }
        
        
        // ATUALIZANDO HABILIDADES
        CalculadoraHabilidades.init();
        // recalculando habilidades
        for (String nomeHabilidade : ficha.getHabilidades().keySet()) {
            int dano = CalculadoraHabilidades.calculaDano(ficha, ficha.getHabilidade(nomeHabilidade));
            ficha.getHabilidade(nomeHabilidade).setDano(dano);
        }
        
        // salvando ficha
//        AlgoritmoPrincipalGerador.putFicha(ficha);
//        try {
//            AlgoritmoPrincipalGerador.saveFicha(new File(algoritmos.batalhas.AlgoritmoPrincipal.getDIR_FICHAS()));
//        } catch (ClassNotFoundException ex) {
//            Logger.getLogger(ViewFicha.class.getName()).log(Level.SEVERE, null, ex);
//        }
        AlgoritmoPrincipal.loadFichas();
        AlgoritmoPrincipal.saveFicha(ficha);
        AlgoritmoPrincipal.putFicha(ficha);
        
//        limparFicha();
        jButtonSalvar.setEnabled(false);
        
        JOptionPane.showMessageDialog(null, "Ficha salva com sucesso!", "Success!", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButtonSalvarActionPerformed

