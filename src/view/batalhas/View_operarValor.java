    private void aplicar() {
        try {
            Ficha personagem = AlgoritmoPrincipal.getFicha();
            
            // se o usuario quiser editar o mop
            if (AlgoritmoPrincipal.getLinha().equals(AlgoritmoPrincipal.getMOP())) {
                
                int mop = personagem.getMopAtual();
                mop += Integer.parseInt(jTextFieldEditarValor.getText());
                
                if (mop > personagem.getMopMax()) {
                    mop = personagem.getMopMax();
                }
                personagem.setMopAtual(mop);
                
            } else { // hp
                
                int hp = personagem.getHpAtual();
                hp += Integer.parseInt(jTextFieldEditarValor.getText());
                
                if (hp > personagem.getHpMax()) {
                    hp = personagem.getHpMax();
                }
                personagem.setHpAtual(hp);
                
                if (personagem.isDesmaiado()) {
                    
                    switch (JOptionPane.showConfirmDialog(null, "Acordar personagem do desmaio?",
                        "Faint", JOptionPane.YES_NO_OPTION)) {
                        
                        case 0:
                        personagem.setDesmaiado(false);
                        break;
                    }
                    
                } else if (personagem.getHpAtual() < 0) {

                    switch (JOptionPane.showConfirmDialog(null, "Desmaiar personagem?",
                        "Faint", JOptionPane.YES_NO_OPTION)) {
                    case 0:
                        personagem.setDesmaiado(true);
                        break;
                    }
                }
                
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Entre com um valor inteiro vÃ¡lido",
                    "Error!", JOptionPane.ERROR_MESSAGE);
            
            return;
        }

        this.dispose();
    }
    
