    private void jTextFieldIgnorarDiasFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldIgnorarDiasFocusLost
        double dias;
        try {
            dias = Double.parseDouble(jTextFieldIgnorarDias.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Ignorar Dias deve ser um numero inteiro positivo válido!",
                    "Error!", JOptionPane.WARNING_MESSAGE);
            
            jTextFieldIgnorarDias.setText("0");
            return;
        }
        // testando se a quantidade de dias setada faz sentido com a qtd de dias restantes de treino e
        // testando se a quantidade de dias setada é positiva
        if (AlgoritmoPrincipalTrainer.isDiasRestantesSetados()) {
            if (dias < 0) {
                JOptionPane.showMessageDialog(null, "A quantidade de dias de treino deve ser positiva!",
                        "Logic Error!", JOptionPane.ERROR_MESSAGE);
                
                AlgoritmoPrincipalTrainer.setDiasIgnorados(0);
                return;
            }
        }
        
        AlgoritmoPrincipalTrainer.setDiasIgnorados(dias);
    }//GEN-LAST:event_jTextFieldIgnorarDiasFocusLost

