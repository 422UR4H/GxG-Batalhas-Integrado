    private void jMenuItemExportFichaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemExportFichaActionPerformed
        Ficha ficha = AlgoritmoPrincipalGerador.getFicha(jTextFieldNick.getText().toUpperCase());
        if (ficha == null) {
            JOptionPane.showMessageDialog(null, "Não foi encontrado personagem com esse nick!",
                    "Error!", JOptionPane.ERROR_MESSAGE);
            
            return;
        }
        
        try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("ficha.dat"))) {
            oos.writeObject(ficha);
            oos.close();
        } catch (IOException e) {
            System.out.println(e.toString());
        }
    }//GEN-LAST:event_jMenuItemExportFichaActionPerformed

