    private void treinarPro() {
        String nomePro = jComboBoxPro.getSelectedItem().toString();
        if (nomePro.equals("NOVA PROFICIENCIA")) {
            // testando se o usuario entrou com algum nome
            nomePro = jTextFieldPro.getText().toUpperCase();
            if (nomePro.equals("")) {
                JOptionPane.showMessageDialog(null, "Insira um nome para a nova proficiência",
                        "Invalid Name!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (nomePro.equals("NENHUMA") || nomePro.equals("NULL") || nomePro.equals("NOVA")) {
                JOptionPane.showMessageDialog(null, "Esse nome não é válido!",
                        "Error!", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            // testando se a pericia já existe
            boolean proficienciaExistente = false;
            for (String nome : AlgoritmoPrincipalTrainer.getFicha().getPro().keySet()) {
                if (nome.equals(nomePro)) {
                    proficienciaExistente = true;
                    break;
                }
            }
            
            if (proficienciaExistente == false) {
                // inserindo nova proficiência
                LvlBonus lvlBonus = new LvlBonus();
                AlgoritmoPrincipalTrainer.getFicha().putPro(nomePro, lvlBonus);
            }
        }
        
        // preparando algoritmo principal para o treino
        AlgoritmoPrincipalTrainer.setNomeTreino(nomePro);
        AlgoritmoPrincipalTrainer.setTreinar(AlgoritmoPrincipalTrainer.getPRO());
        
        // liimpando nome
        jTextFieldPro.setText("");
        
        new View_treinoProEPer(null, true).setVisible(true);
    }
    
