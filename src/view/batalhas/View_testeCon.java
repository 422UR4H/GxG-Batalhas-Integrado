    private void testFaint() {
        int dado;
        try {
            dado = Integer.parseInt(jTextFieldValorDoDado.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Entre com um valor inteiro válido",
                    "Error!", JOptionPane.ERROR_MESSAGE);
            
            jTextFieldValorDoDado.selectAll();
            return;
        }
        if (dado < 1 || dado > 20) {
            JOptionPane.showMessageDialog(null, "O valor do dado deve ser entre 1 e 20!",
                    "Logic Error!", JOptionPane.ERROR_MESSAGE);
            
            jTextFieldValorDoDado.selectAll();
            return;
        }
        
        Ficha personagem = AlgoritmoPrincipal.getPerson(jLabelPersonagem.getText());
        int hpAtual = personagem.getHpAtual();
        int pericia = personagem.getPerCon();
        int teste = Calculadora.getTeste(dado, pericia);
        
        if (teste + hpAtual < 0) {
            personagem.setDesmaiado(true);
            JOptionPane.showMessageDialog(null, "O personagem está desmaiado!",
                    "Character Fainted!", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "O personagem não ficou desmaiado!",
                    "Not Fainted!", JOptionPane.INFORMATION_MESSAGE);
        }
        this.dispose();
    }
    
