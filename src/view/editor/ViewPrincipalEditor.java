    private void jButtonOkPerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkPerActionPerformed
        int lvl;
        
        // lendo novo lvl da jText
        try {
            lvl = Integer.parseInt(jTextFieldLvlPer.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Entre com um número válido!",
                    "Inválid Number!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // setando novo lvl
        try {
            Ficha ficha = AlgoritmoPrincipalEditor.getFicha();
            String nome = jComboBoxPer.getSelectedItem().toString();
            ficha.getPer().get(nome).setLvl(lvl);
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(null, "Entrada Inválida!",
                    "Error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonOkPerActionPerformed

