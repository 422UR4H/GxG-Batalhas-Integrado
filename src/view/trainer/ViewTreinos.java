        for (Integer i : AlgoritmoPrincipalTrainer.getTreinos().keySet()) {
            Object[] vetor = {
                i,
                AlgoritmoPrincipalTrainer.getTreinos().get(i).getNome(),
                AlgoritmoPrincipalTrainer.getTreinos().get(i).getDias()
            };
            
            if (i <= 90) {
                dtm.removeRow(i - 1);
            }
            dtm.insertRow(i - 1, vetor);
        }
        
        
