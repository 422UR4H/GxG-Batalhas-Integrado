    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed
        int valor;
        try {
            valor = Integer.valueOf(jTextField.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Entre com um numero v√°lido",
                    "Error!", JOptionPane.ERROR_MESSAGE);
            
            jTextField.selectAll();
            return;
        }
        AlgoritmoPrincipal.setValor(valor);
        this.dispose();
    }//GEN-LAST:event_jButtonActionPerformed

    private void jTextFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            int valor;
            try {
                valor = Integer.valueOf(jTextField.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Entre com um numero valido", e.toString(), JOptionPane.ERROR_MESSAGE);
                return;
            }
            AlgoritmoPrincipal.setValor(valor);
            this.dispose();
        }
    }//GEN-LAST:event_jTextFieldKeyPressed

